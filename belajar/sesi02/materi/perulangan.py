# for
print('For')
#dengan list
angka = [1,2,3]
for i in angka:
    print(i)

print(40*'-')

#dengan range
for u in range(3): #dimulai dari no (index)
    print(u)
print('\n')
for ui in range(1,3): #dimulai dari satu(bilangan pertama) di akhiri dengan bilangan sebelum angka(contoh 3 menjadi 2)
    print(ui)
    
print(40*'-')

# dengan string
nama = 'Alby'
for n in nama:
    print(n)
print(40*'-')

#while
print('While')
print(40*'-')

angka = 0 # jadi 0 terus di tambah 1 sampai 0 menjadi 5 ( sesuai contoh)
while angka < 5:
    angka += 1
    print(angka)